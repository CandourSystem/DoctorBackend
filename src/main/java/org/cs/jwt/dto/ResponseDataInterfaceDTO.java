package org.cs.jwt.dto;

public class ResponseDataInterfaceDTO {
    String port_name;
    String port_address;
    String host_address;
    String host_port;
    public String getPort_name() {
        return port_name;
    }
    public void setPort_name(String port_name) {
        this.port_name = port_name;
    }
    public String getPort_address() {
        return port_address;
    }
    public void setPort_address(String port_address) {
        this.port_address = port_address;
    }
    public String getHost_address() {
        return host_address;
    }
    public void setHost_address(String host_address) {
        this.host_address = host_address;
    }
    public String getHost_port() {
        return host_port;
    }
    public void setHost_port(String host_port) {
        this.host_port = host_port;
    }
       


   
   
    
}
